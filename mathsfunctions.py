class mathsFunctions:
	def add(a,b):
		return(a+b)
	def subtract(a,b):
		return(a-b)
	def multiply(a,b):
		return(a*b)
	def divide(a,b):
		return(a/b)
	def factorial(a):
		fac=0
		while a>0:
			fac=a*a-1
			a=a-1
		return(fac)
	def power(a,b):
		while b>0:
			ans=a*a
			b=b-1
			
		return(ans)
		
